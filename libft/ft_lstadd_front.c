#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	t_list	*temp;

	if (!lst)
		return ;
	if (!*lst)
		*lst = new;
	else if (*lst && new)
	{
		temp = *lst;
		new->next = temp;
		*lst = new;
	}
}
